static int check_jpeg_format(unsigned char *jpeg)
{
	int i=0;
	for(i=0;i<sizeof(JPEG_HEADER_DAT);i++)
	{
		if (JPEG_HEADER_DAT[i] != jpeg[i])
			return -1;
	}
	
	return 0;
}

